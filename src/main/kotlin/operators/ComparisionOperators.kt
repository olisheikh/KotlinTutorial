package operators

fun main() {
    // Two operand is declared
    var a = 30
    var b = 25

    // We only get boolean value as a result in comparison operators

    // Greater than(>) operator
    println("Greater than: ${a > b}")

    // Greater than and equal (>=) operator
    println("Greater than and equals: ${a >= b}")

    // Less than (<) operator
    println("Less than: ${a < b}")

    // Less than and equal (<=) operator
    println("Less than and equal: ${a <= b}")

    // Equal (==) operator
    println("Equal: ${a == b}")

    // Not equal (!=) operator
    println("Not Equal: ${a != b}")


    // Identity check operator(===)
    println("Identity check operator: ${a === b}")
}