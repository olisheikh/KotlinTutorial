package operators

fun main() {
    // Creating an operand
    var number = 30

    // Post-increment(++)
    print("Post Increment: ${number++}")
    println(" $number")

    // (++)Pre-increment
    println("Pre-increment: ${++number}")

    // Post-decrement(--)
    print("Post decrement: ${number--}")
    println(" $number")

    // (--)Pre-decrement
    println("Pre decrement: ${--number}")
}