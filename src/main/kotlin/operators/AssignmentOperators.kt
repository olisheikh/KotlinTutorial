package operators

fun main() {
    // Assignment(=) operator
    var a = 10

    // Addition(+=) assignment operator
    a += 2
    println("Addition assignment operator: $a")

    // Subtraction(-=) assignment operator
    a -= 2
    println("Subtraction assignment operator: $a")

    // Multiplication(*=) assignment operator
    a *= 2
    println("Multiplication assignment operator: $a")

    // Division(/=) assignment operator
    a /= 2
    println("Division assignment operator: $a")

    // Modulus(%=) assignment operator
    a %= 2
    println("Modulus assignment operator: $a")
}